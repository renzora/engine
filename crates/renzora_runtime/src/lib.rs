//! Renzora Runtime — shared engine library used by every Renzora binary.
//!
//! `add_engine_plugins(app)` iterates the `inventory`-backed plugin
//! registry (populated by `renzora::add!(...)` ctors at dylib load time)
//! and adds every plugin matching the host's scope (Runtime or Editor).
//! Plugins self-register via `renzora::add!(MyPlugin, scope)` in their
//! crate root — no manual enumeration here, no `add_plugins(...)` call
//! to remember.
//!
//! Adding a new plugin:
//! - Add a new crate, write `renzora::add!(MyPlugin)` (or `, Editor` for
//!   editor-only) in its `lib.rs`.
//! - Add the crate as a `[dependencies]` entry of `renzora_runtime` so the
//!   linker pulls it into the binary. Editor-only crates go under the
//!   `editor` feature.
//! - Third-party: ship as a dylib that uses `renzora::add!` — loaded at
//!   runtime by `dynamic_plugin_loader`, no engine source edits required.

use bevy::prelude::*;

pub use renzora;

// Dylib keepalive: `pub use` every plugin crate so cargo emits a real
// dependency edge into this rlib's metadata, which propagates to the
// final binary's DT_NEEDED entries. The plugin dylibs need to actually
// be loaded at startup so their `inventory::submit!` ctors run and
// register entries into the shared plugin registry. The lists are
// generated by build.rs from this crate's own Cargo.toml — adding a new
// plugin only requires editing Cargo.toml, never this file.
include!(concat!(env!("OUT_DIR"), "/engine_reexports.rs"));

#[cfg(feature = "editor")]
mod editor_reexports {
    include!(concat!(env!("OUT_DIR"), "/editor_reexports.rs"));
}
#[cfg(feature = "editor")]
pub use editor_reexports::*;

// ── App setup (single source of truth for engine plugin registration) ────

pub fn platform_wgpu_settings() -> bevy::render::settings::WgpuSettings {
    #[cfg(target_os = "android")]
    {
        use bevy::render::settings::{Backends, WgpuSettings};
        WgpuSettings {
            backends: Some(Backends::VULKAN),
            ..default()
        }
    }

    #[cfg(not(target_os = "android"))]
    {
        use bevy::render::settings::{WgpuFeatures, WgpuSettings};
        WgpuSettings {
            features: WgpuFeatures::POLYGON_MODE_LINE,
            ..default()
        }
    }
}

pub fn init_app() -> App {
    let mut app = App::new();
    renzora_engine::setup_asset_reader(&mut app);
    app
}

pub fn add_default_rendering(app: &mut App) {
    use bevy::render::{settings::RenderCreation, RenderPlugin};
    use bevy::window::{Window, WindowPlugin};
    app.add_plugins(
        DefaultPlugins
            .set(RenderPlugin {
                render_creation: RenderCreation::Automatic(platform_wgpu_settings()),
                ..default()
            })
            .set(ImagePlugin {
                default_sampler: bevy::image::ImageSamplerDescriptor {
                    address_mode_u: bevy::image::ImageAddressMode::Repeat,
                    address_mode_v: bevy::image::ImageAddressMode::Repeat,
                    address_mode_w: bevy::image::ImageAddressMode::Repeat,
                    // Trilinear + 16x anisotropic filtering by default. Bevy's
                    // default sampler runs with `anisotropy_clamp = 1` which
                    // looks blocky on textures viewed at oblique angles
                    // (brick walls, ground planes). Most assets here come from
                    // Sketchfab GLBs without baked mipmaps — anisotropy alone
                    // already cleans up the worst aliasing; full mipmap
                    // generation is a separate piece of work.
                    mag_filter: bevy::image::ImageFilterMode::Linear,
                    min_filter: bevy::image::ImageFilterMode::Linear,
                    mipmap_filter: bevy::image::ImageFilterMode::Linear,
                    anisotropy_clamp: 16,
                    ..default()
                },
                ..default()
            })
            .set(WindowPlugin {
                primary_window: Some(Window {
                    title: "Renzora".into(),
                    decorations: false,
                    resizable: true,
                    ..default()
                }),
                ..default()
            }),
    );
    app.add_systems(Startup, maximize_primary_window);
}

fn maximize_primary_window(
    mut windows: Query<&mut bevy::window::Window, With<bevy::window::PrimaryWindow>>,
) {
    if let Ok(mut window) = windows.single_mut() {
        window.set_maximized(true);
    }
}

/// Adds every engine plugin to the App.
///
/// Foundation plugins are listed explicitly here in dependency order —
/// each one initializes a resource (or otherwise sets up state) that
/// other plugins read during their own `Plugin::build()`. They have to
/// run first, in this exact order. Then the rest of the workspace
/// plugins are auto-discovered via the `inventory` registry — those
/// don't care about ordering and just self-register through
/// `renzora::add!(MyPlugin)` in their crate.
pub fn add_engine_plugins(app: &mut App) {
    // ── Foundation (explicit, ordered) ─────────────────────────────────
    info!("[runtime] foundation: RuntimePlugin");
    app.add_plugins(renzora_engine::RuntimePlugin);
    info!("[runtime] foundation: GlobalsPlugin");
    app.add_plugins(renzora_globals::GlobalsPlugin::default());
    info!("[runtime] foundation: InputPlugin");
    app.add_plugins(renzora_input::InputPlugin::default());
    info!("[runtime] foundation: ScriptingPlugin");
    app.add_plugins(renzora_scripting::ScriptingPlugin::default());
    info!("[runtime] foundation: PhysicsPlugin");
    app.add_plugins(renzora_physics::PhysicsPlugin::default());

    // ── Auto-discovered (any order) ────────────────────────────────────
    renzora::for_each_static_plugin(renzora::PluginScope::Runtime, |plugin| {
        info!("[runtime] static plugin: {}", plugin.name);
        (plugin.install)(app);
    });
}

/// Build the full runtime app (rendering + all engine plugins).
pub fn build_runtime_app() -> App {
    let mut app = init_app();
    add_default_rendering(&mut app);
    add_engine_plugins(&mut app);
    app
}

// ── Editor plugin registration (only present when `editor` feature is on) ─

/// Adds every editor-scope plugin to the App.
///
/// Foundation plugins are listed explicitly here — they initialize the
/// editor's shared registries (`ViewportOverlayRegistry`,
/// `ToolOptionsRegistry`, `ShortcutRegistry`, `KeyBindings`, ...) that
/// downstream editor plugins read during their own `Plugin::build()`.
/// The rest are auto-discovered through `inventory`.
#[cfg(feature = "editor")]
pub fn add_editor_plugins(app: &mut App) {
    // ── Foundation (explicit, ordered) ─────────────────────────────────
    info!("[editor] foundation: AssetRegistryPlugin");
    app.add_plugins(renzora_asset_registry::AssetRegistryPlugin::default());
    info!("[editor] foundation: RenzoraEditorPlugin");
    app.add_plugins(renzora_editor::RenzoraEditorPlugin::default());
    info!("[editor] foundation: KeybindingsPlugin");
    app.add_plugins(renzora_keybindings::KeybindingsPlugin::default());

    // ── Auto-discovered (any order) ────────────────────────────────────
    renzora::for_each_static_plugin(renzora::PluginScope::Editor, |plugin| {
        info!("[editor] static plugin: {}", plugin.name);
        (plugin.install)(app);
    });
}
